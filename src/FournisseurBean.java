public class FournisseurBean {



   private String NumFrs;
    private String RaisonSocale;
    private String ICE;
    private String Adresse;
    private String Telephon;
    private String Email;
    private String ville;

    public String getNumFrs() {
        return NumFrs;
    }

    public void setNumFrs(String NumFrs) {
        this.NumFrs=NumFrs;
    }

    public String getRaisonSocale() {
        return RaisonSocale;
    }

    public void setRaisonSocale(String RaisonSocale) {
        this.RaisonSocale = RaisonSocale;
    }

    public String getICE() {
        return ICE;
    }

    public void setICE(String ICE) {
        this.ICE = ICE;
    }

    public String getAdresse() {
        return Adresse;
    }

    public void setAdresse(String Adresse) {
        this.Adresse = Adresse;
    }

    public String getTelephon() {
        return Telephon;
    }

    public void setTelephon(String Telephon) {
        this.Telephon = Telephon;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getVille() {
        return ville;
    }

    public void setVille(String ville) {
        this.ville = ville;
    }
    

  

}